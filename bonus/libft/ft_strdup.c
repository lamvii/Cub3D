#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		j;
	char	*s;
	char	*dest;

	i = 0;
	s = (char *)src;
	j = ft_strlen(s);
	dest = (char *)malloc(sizeof(char) * (j + 1));
	if (dest == NULL)
		return (NULL);
	else
	{
		while (s[i])
		{
			dest[i] = s[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (dest);
}
